// ─────────────────────────────────────────────────────────────────────────────
// Partner-room internationalization.
//
// One dictionary per guest-facing language. The room's `locale` column selects
// which dictionary renders. To ADD A LANGUAGE: (1) add its code to the
// `room_locale` pg enum + a migration, (2) add an entry to ROOM_LOCALE_OPTIONS
// below, (3) add a `<code>: { … }` dictionary matching the RoomDict shape. The
// CRM language dropdown and everything downstream pick it up automatically.
//
// `es` is the canonical shape (RoomDict = typeof es); every other dictionary is
// typed `RoomDict`, so TypeScript fails the build if a key is missing or a
// plural/interpolation signature drifts. That's the guarantee against half-
// translated UI.
// ─────────────────────────────────────────────────────────────────────────────

// `dir` drives RTL layout on the guest room (Arabic). `autoTranslate` marks a
// CLIENT-ONLY language: operators author content in es/en, so content shown to
// a pt/ru/ar guest is machine-translated (Claude) + cached. es/en are the
// operator's own languages → content renders as authored (no translation).
export const ROOM_LOCALE_OPTIONS = [
  { value: "es", label: "Español", englishName: "Spanish", flag: "🇪🇸", intl: "es-VE", dir: "ltr", autoTranslate: false },
  { value: "en", label: "English", englishName: "English", flag: "🇬🇧", intl: "en-US", dir: "ltr", autoTranslate: false },
  { value: "pt", label: "Português", englishName: "Brazilian Portuguese", flag: "🇧🇷", intl: "pt-BR", dir: "ltr", autoTranslate: true },
  { value: "ru", label: "Русский", englishName: "Russian", flag: "🇷🇺", intl: "ru-RU", dir: "ltr", autoTranslate: true },
  // Arabic renders right-to-left. Western digits (ar-u-nu-latn) suit business /
  // diplomatic readers better than Eastern Arabic numerals.
  { value: "ar", label: "العربية", englishName: "Modern Standard Arabic", flag: "🇸🇦", intl: "ar-u-nu-latn", dir: "rtl", autoTranslate: true },
] as const;

export type RoomLocale = (typeof ROOM_LOCALE_OPTIONS)[number]["value"];
export type RoomDir = "ltr" | "rtl";

export function resolveRoomLocale(value: string | null | undefined): RoomLocale {
  return ROOM_LOCALE_OPTIONS.some((o) => o.value === value)
    ? (value as RoomLocale)
    : "es";
}

/** Full option metadata (label, flag, intl tag, dir, autoTranslate) for a locale. */
export function roomLocaleMeta(locale: string | null | undefined) {
  return ROOM_LOCALE_OPTIONS.find((o) => o.value === resolveRoomLocale(locale))!;
}

/** BCP-47 tag for Intl/toLocale* formatting in the given room locale. */
export function roomIntlLocale(locale: string | null | undefined): string {
  return roomLocaleMeta(locale).intl;
}

/** Text direction for the room shell — "rtl" for Arabic, "ltr" otherwise. */
export function roomDir(locale: string | null | undefined): RoomDir {
  return roomLocaleMeta(locale).dir;
}

/**
 * Whether operator-authored content (welcome message, next steps, doc labels,
 * chat) should be machine-translated for this locale. True for client-only
 * languages (pt/ru/ar); false for the operator's own es/en, which render as
 * authored. See lib/partner-room-translate.server.ts.
 */
export function roomAutoTranslates(locale: string | null | undefined): boolean {
  return roomLocaleMeta(locale).autoTranslate;
}

